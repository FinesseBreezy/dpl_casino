def draw(a,b)
  push_frame = @values.length
  puts "\n" * push_frame.to_i
  a = a
  b = b
  @values[a][b] = "╬"
  # Loop over each row Array.
  @values.each do |y|

    # Loop over each cell in the row.
    y.each do |cell|
      print cell
    end


    # End of row.
    puts ""
  end
end

require 'io/console'

# Reads kexpresses from the user including 2 and 3 escape character sequences.
def read_char
  STDIN.echo = false
  STDIN.raw!

  input = STDIN.getc.chr
  if input == "\e" then
    input << STDIN.read_nonblock(3) rescue nil
    input << STDIN.read_nonblock(2) rescue nil
  end
ensure
  STDIN.echo = true
  STDIN.cooked!

  return input
end
@y = 10
@x = 10
def move()
  c = read_char
  puts c
  case c
    #up
  when "\e[A"
    if @y <= 0
      @y += 1
    end
    @y -= 1
    if @values[@y][@x] == "░"
      @values[@y + 1][@x] = "░"
    else
      @values[@y + 1][@x] = "▓"
    end
    #down
  when "\e[B"
    if @y >= @values.length - 1
      @y -= 1
    end
    @y += 1
    #CARPET CHECK
    if @values[@y][@x] == "░"
      @values[@y - 1][@x] = "░"
    else
      @values[@y - 1][@x] = "▓"
    end
    #right
  when "\e[C"
    if @x >= @values[0].length - 1
      @x -= 1
    end
    @x += 1
    #CARPET CHECK
    if @values[@y][@x] == "░"
      @values[@y][@x - 1] = "░"
    else
      @values[@y][@x - 1] = "▓"
    end
    #left
  when "\e[D"
    if @x <= 0
      @x += 1
    end
    @x -= 1
    if @values[@y][@x] == "░"
      @values[@y][@x + 1] = "░"
    else
      @values[@y][@x + 1] = "▓"
    end
  when "\u0003"
    puts "CONTROL-C"
    exit 0
  end
  system ("cls")
  draw(@y,@x)
end
while true
  move
end
# oringal case statement from:
# http://www.alecjacobson.com/weblog/?p=75
def show_single_kex
  c = read_char

  case c
  when " "
    puts "SPACE"
  when "\t"
    puts "TAB"
  when "\r"
    puts "RETURN"
  when "\n"
    puts "LINE FEED"
  when "\e"
    puts "ESCAPE"
  when "\e[A"
    puts "UP ARROW"
  when "\e[B"
    puts "DOWN ARROW"
  when "\e[C"
    puts "RIGHT ARROW"
  when "\e[D"
    puts "LEFT ARROW"
  when "\177"
    puts "BACKSPACE"
  when "\004"
    puts "DELETE"
  when "\e[3~"
    puts "ALTERNATE DELETE"
  when "\u0003"
    puts "CONTROL-C"
    exit 0
  when /^.$/
    puts "SINGLE CHAR HIT: #{c.inspect}"
  else
    puts "SOMETHING ELSE: #{c.inspect}"
  end
end

show_single_kex while(true)
